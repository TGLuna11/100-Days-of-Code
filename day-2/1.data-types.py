# String
sentence = "Hello World"
# Integer
integer = 11
# Float
decimal = 7.5
# Boolean
is_valid = True

# Subscripting
print(sentence[0])  # This will print the first letter of the sentence
print(sentence[-1])  # This will print the last letter of the sentence

# Printing the data types of the variables
print(type(sentence))
print(type(integer))
print(type(decimal))
print(type(is_valid))

# Data type conversion
num = 5
print(type(num))  # Integer
num = str(num)
print(type(num))  # String

number_as_string = "123"
print(type(number_as_string))  # String
number_as_string = int(number_as_string)
print(type(number_as_string))  # Integer
