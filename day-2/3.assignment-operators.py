# Assignment operators
score = 10  # This assigns the value 10 to the variable score
score += 1  # This assigns the value of score + 1 to the variable score
score -= 1  # This assigns the value of score - 1 to the variable score
score *= 2  # This assigns the value of score * 2 to the variable score
score /= 2  # This assigns the value of score / 2 to the variable score

score = 10
