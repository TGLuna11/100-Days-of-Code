# Logical operators
# AND, OR, NOT are logical operators that are used to combine conditional statements

number = 11

# AND: Returns True if both statements are true
is_and = number > 10 and number < 20  # This is True because 11 is greater than 10 and less than 20

# OR: Returns True if one of the statements is true
is_or = number > 10 or number < 20  # This is True because 11 is greater than 10

# NOT: Returns False if the result is true
is_not = not number > 10  # This is False because 11 is greater than 10 and the not operator inverts the result
